import math

print(round(1.3))  # se acerca mas al 1 el inferior
print(round(1.7))  # se acerca mas al 2 el superior
print(round(1.5))  # se acerca mas al 2
print(abs(-77))  # devuelve numero positivo
print(abs(55))  # devuelve numero positivo

print(math.ceil(1.1))  # numero entero mas cercano hacia arriba
print(math.floor(1.2555))  # numero entero mas cercano hacia abajo
print(math.isnan(23))  # nos dice si es un numero o no lo que le pasamos
# print(math.isnan("23"))
print(math.pow(10, 3))  # eleva a la potencia un numero
print(math.sqrt(9))  # nos saca la raiz cuadrada del numero que le pasemos
